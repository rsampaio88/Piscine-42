#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	a;

	a = argc - 1;
	i = 0;
	while (a != 0)
	{
		while (argv[a][i] != '\0')
		{
			write(1, &argv[a][i], 1);
			i++;
		}
		i = 0;
		a--;
		write(1, "\n", 1);
	}
	return (0);
}
